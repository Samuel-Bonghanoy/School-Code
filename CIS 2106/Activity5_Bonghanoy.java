public class Activity5_Bonghanoy {
  public static void main(String[] args) {
    int num1 = 10;
    int num2 = 5;

 
    System.out.println("Number 1: " + num1);
    System.out.println("Number 2: " + num2);


    int sum = num1 + num2;
    System.out.println("Result of addition: " + num1 + " + " + num2 + " = " + sum);


    int difference = num1 - num2;
    System.out.println("Result of subtraction: " + num1 + " - " + num2 + " = " + difference);

  
    int product = num1 * num2;
    System.out.println("Result of multiplication: " + num1 + " * " + num2 + " = " + product);

   
    double quotient = (double) num1 / num2;
    System.out.println("Result of division: " + num1 + " / " + num2 + " = " + quotient);
}
}
